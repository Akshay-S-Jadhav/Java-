
public class datatypes {
    public static void main(String[] args) {
       System.out.println("The following are all datatypes examples");

       byte b = 1; // Its size is 1Byte and range of -128 to 127 
       System.out.println("This is byte data type : " + b);

       short s = 31000; // Its size is 2Byte and range of -32k to 32k
       System.out.println("This is short data type : " + s);

       int i = 5;
       System.out.println("This is int data type : " + i);

       long l = 9223372;
       System.out.println("This is long data type : " + l);

       float pi; 
       pi = 3.14f;
       System.out.println("This is float data type : " + pi);

       double d = 3.1273648263281243;
       System.out.println("This is double data type : " + d);

       char c = 'a';
       System.out.println("This is char data type : " + c);

       boolean bl = true;
       System.out.println("This is boolean data type : " + bl);

    }
}